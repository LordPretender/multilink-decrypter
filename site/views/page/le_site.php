			<h3 class="section-heading">Le site</h3>
			<hr class="sep" />
			
			<p>
				Un site Multi-Uploader (ou Multi-Links) est un site qui permet, en quelques clics seulement, d'héberger un fichier sur plusieurs plateformes de stockage simultanément. 
				<br />
				Ainsi, via un seul lien, l'utilisateur a à sa disposition une liste de liens vers diverses plateformes.
			</p>
			
			<p>
				Ce genre de sites présente un gros inconvénient qui concerne les utilisateurs, non pas ceux qui upload, mais ceux qui téléchargent. 
				Le lien Multi-Links ouvre de nombreuses fenêtres intempestives et comportent un grand nombre de bannières.
				<br />
				Non seulement les liens ne sont pas mis en évidence afin de permettre une utilisation aisée, mais en plus, il n'est pas possible de savoir si un lien est encore valide ou non (si le fichier est encore stocké sur une plateforme de stockage).
			</p>
			
			<p>Pour résumer, bien que le concept de ce genre de sites soit intéressant, ils ont été conçus pour que les webmasters gagnent le plus d'argent possible au détriment des utilisateurs.</p>
			
			<p>
				MultiLinks-Decrypter a été conçu afin de vous simplifier la vie et de vous faire gagner du temps. 
				En effet, nous récupérons les liens pour vous et nous mettons en évidence les liens valides.
				<br />
				Nous aussi disposons de bannières publicitaires mais elles ne sont pas envahissantes, les liens restent accessibles et lisibles. Avec nous, vous n'aurez aucune surprise.
			</p>
