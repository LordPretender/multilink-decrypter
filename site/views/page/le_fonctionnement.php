			<h3 class="section-heading">Le fonctionnement</h3>
			<hr class="sep" />
			
			<ul class="sc-tabs nostyle">
				<li class="title">Technologies</li>
				<li class="tab">
					<p>Le site a été développé sous CodeIgniter et utilise donc les langages suivants : PHP, HTML, CSS, AJAX.</p>
					<p>Le thème provient de ThemeForest et a été légèrement adapté pour les besoins de ce projet car présentait beaucoup d'éléments superflus.</p>
					<p>Pour le bon fonctionnement, Javascript doit être activé sur votre navigateur.</p>
				</li><!-- .tab -->

				<li class="title">Décrypter</li>
				<li class="tab">
					<p>Les liens sont décryptés en chargeant la page sur le serveur afin d'y effectuer une extraction directement via le code source.</p>
					<p>
						Le code HTML diffère entre chaque sites Multi-Links et nécessite donc parfois des traitements supplémentaires. 
						Ainsi, n'hésitez pas à nous contacter si le service ne fonctionne pas correctement.
					</p>
				</li><!-- .tab -->

				<li class="title">Vérifier</li>
				<li class="tab">
					<p>Ce service permet de tester la validité (ou non) des liens de téléchargement via plateforme de stockage.</p>
					<p>Bien que cela ne soit pas compliqué à gérer, nous avons décidé de sous-traiter ce service. Nous utilisons l'API de urlchecker.net</p>
					<p>
						Ainsi, pour optimiser les échanges entre nos deux sites, il a été décidé que la vérification sera réalisée manuellement par l'utilisateur. 
						En effet, en règle général, un seul lien valide suffit et il n'est donc pas nécessaire de tester tous les liens.
					</p>
				</li><!-- .tab -->
			</ul>
